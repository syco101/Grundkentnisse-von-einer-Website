    <p1> Bitcoin ist eine dezentrale
         digitale Währung, die 2009 von einer unbekannten Person oder Gruppe 
         unter dem Pseudonym Satoshi Nakamoto
        eingeführt wurde. Sie basiert auf einer 
        Technologie namens Blockchain, die Transaktionen
         in einer öffentlichen, verteilten Datenbank aufzeichnet.</p1>
<br> 
<br>

         <p1> 
            
            Merkmale

            
         </p1> 
            <p1> 
                 Dezentralisierung:

            <br><br>

            Bitcoin wird nicht von einer
             zentralen Institution wie einer Bank oder Regierung kontrolliert.
             Stattdessen wird das Netzwerk von einer Vielzahl von Computern,
             sogenannten Nodes, betrieben, die weltweit verteilt sind.

             
            </p1> 
            <p1> 
             Blockckchain-Technologie:

             <br><br>

            Die Blockchain ist ein öffentliches, unveränderliches Register, das alle Transaktionen speichert.
            Neue Transaktionen werden in Blöcken zusammengefasst und chronologisch angeordnet.
            Jeder Block enthält eine Referenz zum vorherigen Block,
            wodurch eine Kette entsteht. Dies gewährleistet die Integrität und Sicherheit der Transaktionshistorie.

            
        </p1>

        <p1> 
            Transparenz und Anonymität:

            <br><br>

            Transaktionen sind transparent, da sie in der Blockchain öffentlich einsehbar sind. 
            Dennoch bleibt die Identität der Nutzer anonym, da sie durch kryptografische Adressen (Wallets) repräsentiert werden.

           

        </p1>
        <p1> 
            Limitierte Gesamtmenge:

            <br><br>

            Die maximale Anzahl an Bitcoins ist auf 21 Millionen begrenzt.
             Diese Begrenzung soll Inflation vorbeugen.
            
